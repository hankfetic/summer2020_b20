package Day0;
import java.util.Scanner;
public class forloop {
    public static void main(String[] args) {


        for (int i = 0; i<30; i++ ){
            System.out.print(i+ " ");
        }
        System.out.println("============================================================");

        for (int i = 30; i>0; i--)
            System.out.print(i + " ");

    }
}
